# 15. Escreva um programa que concatene duas strings fornecidas pelo usuário.

string1_usuario = input("Digite o que quiser: ")
string2_usuario = input("Digite o que quiser novamente: ")

print(f"O que você digitou primeiro foi '{string1_usuario}' e depois foi '{string2_usuario}'. Juntando tudo o que você digitou temos o seguinte: {string1_usuario + " " + string2_usuario}. ")